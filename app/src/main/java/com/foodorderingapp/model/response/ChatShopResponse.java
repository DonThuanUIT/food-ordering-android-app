package com.foodorderingapp.model.response;

public class ChatShopResponse {
    private String id;
    private String name;
    private String logoUrl;
    private String coverUrl;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getLogoUrl() {
        return logoUrl;
    }

    public String getCoverUrl() {
        return coverUrl;
    }
}
