package com.foodorderingapp.model.response;

public class ChatUserResponse {
    private String id;
    private String phone;
    private String fullName;
    private String role;

    public String getId() {
        return id;
    }

    public String getPhone() {
        return phone;
    }

    public String getFullName() {
        return fullName;
    }

    public String getRole() {
        return role;
    }
}
